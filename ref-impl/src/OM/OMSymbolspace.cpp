const wchar_t* 
OMSymbolspace::getSymbol(OMClassId id) const
{
    TRACE("OMSymbolspace::getSymbol");
    
    OMWString* symbol;
    if (_idToSymbol.find(id, &symbol))
    {
        return symbol->c_str();
    }
    
    return 0;
}

